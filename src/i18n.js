function setLanguage(lang) {
    localStorage.setItem('preferredLanguage', lang);
    document.documentElement.lang = lang;
    
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (el.tagName.toLowerCase() === 'img') {
                el.alt = translations[lang][key];
            } else {
                el.innerHTML = translations[lang][key];
            }
        }
    });

    // Update placeholders
    const inputs = document.querySelectorAll('[data-i18n-placeholder]');
    inputs.forEach(input => {
        const key = input.getAttribute('data-i18n-placeholder');
        if (translations[lang] && translations[lang][key]) {
            input.placeholder = translations[lang][key];
        }
    });

    // Update active state in language switcher
    const langLinks = document.querySelectorAll('.lang-link');
    langLinks.forEach(link => {
        if (link.getAttribute('data-lang') === lang) {
            link.classList.add('text-secondary', 'border-b', 'border-secondary', 'font-bold');
            link.classList.remove('text-slate-400');
        } else {
            link.classList.remove('text-secondary', 'border-b', 'border-secondary', 'font-bold');
            link.classList.add('text-slate-400');
        }
    });
}

// Initialize language on load
window.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLanguage') || 'en';
    setLanguage(savedLang);
    
    // Add event listeners to language links
    const langLinks = document.querySelectorAll('.lang-link');
    langLinks.forEach(link => {
        link.addEventListener('click', (e) => {
            e.preventDefault();
            const lang = link.getAttribute('data-lang');
            setLanguage(lang);
        });
    });
});
